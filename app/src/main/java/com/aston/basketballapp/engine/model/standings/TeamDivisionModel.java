package com.aston.basketballapp.engine.model.standings;

public class TeamDivisionModel {
    String name;
    String rank;
    String win;
    String loss;
    String gamesBehind;

    public String getName() {
        return name;
    }

    public String getRank() {
        return rank;
    }

    public String getWin() {
        return win;
    }

    public String getLoss() {
        return loss;
    }

    public String getGamesBehind() {
        return gamesBehind;
    }
}
